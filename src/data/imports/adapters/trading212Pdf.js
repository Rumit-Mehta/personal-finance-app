function parseInstrumentPairs(sectionText) {
  return [...sectionText.matchAll(/\b([A-Z][A-Z0-9.]{0,9})\s+([A-Z]{2}[A-Z0-9]{10})\b/gu)]
    .map((match) => ({
      instrument: match[1],
      isin: match[2],
      currency: currencyNear(sectionText, match.index),
    }))
    .filter((item, index, items) => {
      const firstIndex = items.findIndex(
        (candidate) => candidate.instrument === item.instrument && candidate.isin === item.isin,
      );

      return firstIndex === index;
    });
}

function currencyNear(sectionText, startIndex = 0) {
  const nearby = sectionText.slice(startIndex, startIndex + 160);

  return nearby.match(/\b(GBP|GBX|USD|EUR|CAD|CHF)\b/u)?.[1] ?? "";
}

function quantitiesAfterHeading(sectionText) {
  const block = sectionAfter(sectionText, "QUANTITY").split(/\bPRICE\b/u)[0] ?? "";

  return numericValues(block);
}

function valuesAfterLastValueHeading(sectionText) {
  const parts = sectionText.split(/\bVALUE\b/u);

  return numericValues(parts.at(-1) ?? "");
}

function valuesAfterValueGbpHeadings(sectionText) {
  return sectionText
    .split(/VALUE \(GBP\)/u)
    .slice(1)
    .flatMap((part) => numericValues(part.split(/CUSTOMER ID|INSTRUMENT ISIN/u)[0] ?? ""));
}

function categoryForTransactionType(type) {
  if (/interest|dividend|cashback/iu.test(type)) {
    return "income";
  }

  if (/purchase|fee|withdrawal/iu.test(type)) {
    return "expense";
  }

  if (/deposit|truelayer/iu.test(type)) {
    return "transfer";
  }

  return "";
}

function sectionBetween(value, start, endPattern) {
  const startIndex = value.indexOf(start);

  if (startIndex < 0) {
    return "";
  }

  const rest = value.slice(startIndex + start.length);
  const endMatch = rest.match(endPattern);

  return endMatch?.index === undefined ? rest : rest.slice(0, endMatch.index);
}

function sectionAfter(value, marker) {
  const index = value.indexOf(marker);

  return index < 0 ? "" : value.slice(index + marker.length);
}

function lines(value) {
  return String(value ?? "")
    .split(/\r?\n/u)
    .map((line) => line.replace(/\s+/gu, " ").trim())
    .filter(Boolean);
}

function allMatches(value, pattern) {
  return [...String(value ?? "").matchAll(pattern)].map((match) => match[0]);
}

function numericValues(value) {
  return [...String(value ?? "").matchAll(/(?<![:\d])-?[\d,]+(?:\.\d+)?(?![:\d])/gu)]
    .map((match) => money(match[0]))
    .filter((value) => Number.isFinite(value));
}

function matchOne(value, pattern) {
  return String(value ?? "").match(pattern)?.[1] ?? "";
}

function matchMoney(value, pattern) {
  const match = String(value ?? "").match(pattern);

  return match ? money(match[1]) : null;
}

function statementDateTimeToIso(value) {
  const match = text(value).match(
    /(?:(\d{1,2})\s+([A-Za-z]+)\s+(\d{4})|(\d{2})\.(\d{2})\.(\d{4}))\s*(?:at\s*)?(\d{2}:\d{2})?/iu,
  );

  if (!match) {
    return "";
  }

  if (match[1]) {
    return `${match[3]}-${monthNumber(match[2])}-${match[1].padStart(2, "0")}T00:00:00.000Z`;
  }

  return `${match[6]}-${match[5]}-${match[4]}T${match[7] ?? "00:00"}:00.000Z`;
}

function statementDateToIsoDate(value) {
  return statementDateTimeToIso(value).slice(0, 10);
}

function monthNumber(value) {
  const monthIndex = [
    "january",
    "february",
    "march",
    "april",
    "may",
    "june",
    "july",
    "august",
    "september",
    "october",
    "november",
    "december",
  ].indexOf(text(value).toLowerCase());

  return String(monthIndex + 1).padStart(2, "0");
}

function money(value) {
  const parsed = Number(String(value ?? "").replace(/[£,\s]/gu, ""));

  return Number.isFinite(parsed) ? parsed : 0;
}

function formatMoney(value) {
  return `GBP ${Number(value).toFixed(2)}`;
}

function text(value) {
  return String(value ?? "").trim();
}

function escapeRegExp(value) {
  return text(value).replace(/[\\^$.*+?()[\]{}|]/gu, "\\$&");
}
