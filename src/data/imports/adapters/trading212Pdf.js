const PROVIDER = "trading212";
const SOURCE_TYPE = "trading212-pdf";
const ACCOUNT_CONFIGS = [
  {
    key: "invest",
    label: "Invest",
    displayName: "Trading 212 Invest",
    type: "investment",
  },
  {
    key: "stocks-isa",
    label: "Stocks ISA",
    displayName: "Trading 212 Stocks ISA",
    type: "stocks-isa",
  },
  {
    key: "cash-isa",
    label: "Cash ISA",
    displayName: "Trading 212 Cash ISA",
    type: "cash-isa",
  },
  {
    key: "cfd",
    label: "CFD",
    displayName: "Trading 212 CFD",
    type: "cfd",
  },
];
const TRANSACTION_TYPES = [
  "Interest on cash",
  "Card purchase",
  "Card cashback",
  "TrueLayer",
  "ADR fee",
  "Deposit",
  "Withdrawal",
  "Cashback",
  "FX fee",
  "Interest paid",
];
const SECTION_MATCHERS = [
  ["executed-trades", "executed trades"],
  ["open-positions", "open positions summary"],
  ["cash-breakdown", "cash breakdown"],
  ["transactions", "transactions"],
];

export const trading212PdfAdapter = {
  id: "trading212-pdf",
  sourceType: SOURCE_TYPE,
  sourceProvider: PROVIDER,

  detect(input = {}) {
    const value = text(input.text ?? input.statementText);

    return value.includes("Trading 212") && (isMonthlyStatement(value) || isAnnualStatement(value));
  },

  createRawBatch({ parsedPdf, file, fileHash, importedAt }) {
    if (!this.detect(parsedPdf)) {
      throw new Error("Unsupported PDF format. Expected a Trading 212 monthly or annual statement.");
    }

    return {
      adapterId: this.id,
      sourceType: this.sourceType,
      sourceProvider: this.sourceProvider,
      fileName: file?.name || "",
      fileHash,
      importedAt,
      pageCount: parsedPdf.pageCount ?? parsedPdf.pages?.length ?? 0,
      pages: parsedPdf.pages ?? [parsedPdf.text ?? ""],
      pageRows: parsedPdf.pageRows ?? [],
      statementText: parsedPdf.text ?? (parsedPdf.pages ?? []).join("\n\n"),
    };
  },

  normalize(rawBatch) {
    return normalizeTrading212Statement(rawBatch);
  },
};

function isMonthlyStatement(value) {
  return value.includes("Monthly statement") && value.includes("Generated by Trading 212");
}

function isAnnualStatement(value) {
  return (
    /Annual Statement\s*-\s*\d{4}\s*\/\s*\d{4}/iu.test(value) &&
    /This statement covers the period from/iu.test(value)
  );
}

export function normalizeTrading212Statement(rawBatch) {
  if (isAnnualStatement(rawBatch.statementText)) {
    return normalizeTrading212AnnualStatement(rawBatch);
  }

  const statement = parseStatementMetadata(rawBatch.statementText);
  const accountIds = parseAccountIds(rawBatch.statementText);
  const overview = parseOverview(rawBatch.statementText);
  const groupedPages = groupPagesBySection(rawBatch.pages ?? []);
  const accounts = [];
  const balances = [];
  const rows = [];
  const investments = [];
  const valueHistory = [];
  const warnings = [];

  ACCOUNT_CONFIGS.forEach((config, index) => {
    const accountId = accountIds[config.key] || config.key;
    const account = createAccount(config, accountId);
    const accountOverview = overview[index] ?? {};
    const grouped = groupedPages.get(config.key) ?? new Map();
    const cashPages = grouped.get("cash-breakdown") ?? [];
    const transactionPages = grouped.get("transactions") ?? [];
    const tradePages = grouped.get("executed-trades") ?? [];
    const positionPages = grouped.get("open-positions") ?? [];
    const cashBreakdown = parseCashBreakdown(
      cashPages.join("\n"),
      account,
      accountOverview,
      statement,
    );
    const parsedPositions = parseOpenPositions(
      positionPages.join("\n"),
      account,
      statement,
    );

    if (
      !cashBreakdown.hasCash &&
      transactionPages.length === 0 &&
      tradePages.length === 0 &&
      parsedPositions.investments.length === 0 &&
      Object.keys(accountOverview).length === 0
    ) {
      return;
    }

    accounts.push(account);

    if (cashBreakdown.balance) {
      balances.push(cashBreakdown.balance);
    }

    rows.push(
      ...parseCashTransactions(transactionPages, account, statement),
      ...parseExecutedTrades(tradePages.join("\n"), account, statement),
      ...createDividendSummaryRows(accountOverview, account, statement),
    );
    investments.push(...parsedPositions.investments);
    valueHistory.push(...parsedPositions.valueHistory);
    warnings.push(...cashBreakdown.warnings, ...parsedPositions.warnings);
  });

  if (accounts.length === 0) {
    throw new Error("Trading 212 statement did not contain importable accounts.");
  }

  return {
    adapterId: trading212PdfAdapter.id,
    sourceType: SOURCE_TYPE,
    sourceProvider: PROVIDER,
    fileName: rawBatch.fileName,
    fileHash: rawBatch.fileHash,
    importedAt: rawBatch.importedAt,
    statement,
    accounts,
    balances,
    rows,
    investments,
    valueHistory,
    warnings,
    allowAccountRetarget: false,
  };
}

function normalizeTrading212AnnualStatement(rawBatch) {
  const statement = parseStatementMetadata(rawBatch.statementText);
  const parsedBalances = parseAnnualOverviewBalances(rawBatch);
  const accounts = parsedBalances.map(({ account }) => account);
  const balances = parsedBalances.map(({ account, balance }) =>
    createAnnualBalanceSnapshot(account, balance, statement),
  );

  if (accounts.length === 0) {
    throw new Error("Trading 212 annual statement did not contain importable account balances.");
  }

  return {
    adapterId: trading212PdfAdapter.id,
    sourceType: SOURCE_TYPE,
    sourceProvider: PROVIDER,
    fileName: rawBatch.fileName,
    fileHash: rawBatch.fileHash,
    importedAt: rawBatch.importedAt,
    statement,
    accounts,
    balances,
    rows: [],
    investments: [],
    valueHistory: [],
    warnings: [],
    allowAccountRetarget: false,
  };
}

function parseAnnualOverviewBalances(rawBatch) {
  const rows = annualOverviewRows(rawBatch);
  const headerRow = rows.find((row) => accountColumnsFromRow(row).length >= 2);

  if (!headerRow) {
    return [];
  }

  const columns = accountColumnsFromRow(headerRow);

  return columns
    .map((column, index) => {
      const sourceId = annualColumnAccountId(rows, column, columns[index + 1]);
      const balance = annualColumnBalance(rows, column, columns[index + 1]);

      if (!sourceId || !Number.isFinite(balance)) {
        return null;
      }

      return {
        account: createAccount(column.config, sourceId),
        balance,
      };
    })
    .filter(Boolean);
}

function annualOverviewRows(rawBatch) {
  const pages = rawBatch.pages ?? [];
  const pageRows = rawBatch.pageRows ?? [];
  const pageIndex = pages.findIndex(
    (pageText) => /Annual Statement\s*-\s*\d{4}\s*\/\s*\d{4}/iu.test(pageText) &&
      pageText.includes("Overview"),
  );

  return normalizeLayoutRows(pageRows[pageIndex >= 0 ? pageIndex : 0] ?? []);
}

function accountColumnsFromRow(row) {
  return row.items
    .map((item) => ({
      x: item.x,
      config: ACCOUNT_CONFIGS.find(
        (config) => text(config.displayName).toLowerCase() === text(item.text).toLowerCase(),
      ),
    }))
    .filter((column) => column.config)
    .sort((left, right) => left.x - right.x);
}

function annualColumnAccountId(rows, column, nextColumn) {
  for (const row of rows) {
    const match = annualColumnText(row, column, nextColumn).match(/Account ID:\s*(\d+)/u);

    if (match) {
      return match[1];
    }
  }

  return "";
}

function annualColumnBalance(rows, column, nextColumn) {
  for (const row of rows) {
    const match = annualColumnText(row, column, nextColumn).match(
      /\b(?:Account value|Account balance)\s+(?:\u00c2?\u00a3\s*)?(-?[\d,]+(?:\.\d+)?)/u,
    );

    if (match) {
      return money(match[1]);
    }
  }

  return null;
}

function annualColumnText(row, column, nextColumn) {
  return row.items
    .filter((item) => item.x >= column.x - 4 && (!nextColumn || item.x < nextColumn.x - 4))
    .map((item) => item.text)
    .join(" ")
    .replace(/\s+/gu, " ")
    .trim();
}

function normalizeLayoutRows(rows) {
  return rows
    .map((row) => ({
      y: Number(row.y ?? 0),
      items: (row.items ?? [])
        .map((item) => ({
          text: text(item.text),
          x: Number(item.x ?? 0),
          y: Number(item.y ?? row.y ?? 0),
        }))
        .filter((item) => item.text)
        .sort((left, right) => left.x - right.x),
    }))
    .filter((row) => row.items.length > 0);
}

function createAnnualBalanceSnapshot(account, balance, statement) {
  const notes = [
    "Trading 212 annual statement.",
    statement.periodStart && statement.periodEnd
      ? `Statement period: ${statement.periodStart} to ${statement.periodEnd}.`
      : "",
  ]
    .filter(Boolean)
    .join("\n");

  return {
    id: `${SOURCE_TYPE}:${account.sourceId}:annual:${statement.statementDate}`,
    accountId: account.id,
    accountRole: account.accountRole,
    date: statement.statementDate,
    balance,
    currency: "GBP",
    sourceType: SOURCE_TYPE,
    sourceProvider: PROVIDER,
    sourceId: `${account.sourceId}:annual-account-value`,
    notes,
  };
}

function parseStatementMetadata(statementText) {
  const generatedAt = matchOne(
    statementText,
    /Generated by Trading 212[^\n,]*?\bon\s+([^,\n]+)/u,
  );
  const [, startValue = "", endValue = ""] =
    statementText.match(/covering from ([^)]+?) \(UTC\) to ([^)]+?) \(UTC\)/u) ??
    [];
  const monthlyActivityPeriod = parseMonthlyActivityPeriod(statementText);
  const annualPeriod = parseAnnualStatementPeriod(statementText);
  const periodStart =
    statementDateTimeToIso(startValue) ||
    monthlyActivityPeriod.periodStart ||
    annualPeriod.periodStart;
  const periodEnd =
    statementDateTimeToIso(endValue) ||
    monthlyActivityPeriod.periodEnd ||
    annualPeriod.periodEnd;
  const endDate =
    statementDateToIsoDate(endValue) ||
    monthlyActivityPeriod.statementDate ||
    annualPeriod.statementDate ||
    new Date().toISOString().slice(0, 10);

  return {
    generatedAt: statementDateToIsoDate(generatedAt),
    periodStart,
    periodEnd,
    statementDate: endDate,
  };
}

function parseAnnualStatementPeriod(statementText) {
  const match = text(statementText).match(
    /This statement covers the period from (.+?) \(UTC\) to (.+?) \(UTC\)/iu,
  );

  if (!match) {
    return {
      periodStart: "",
      periodEnd: "",
      statementDate: "",
    };
  }

  const periodStart = statementDateTimeToIso(match[1]);
  const periodEnd = statementDateTimeToIso(match[2]);

  return {
    periodStart,
    periodEnd,
    statementDate: periodEnd.slice(0, 10),
  };
}

function parseMonthlyActivityPeriod(statementText) {
  const match = text(statementText).match(/covering all ([A-Za-z]+)\s+(\d{4}) activity/iu);

  if (!match) {
    return {
      periodStart: "",
      periodEnd: "",
      statementDate: "",
    };
  }

  const month = monthNumber(match[1]);
  const year = Number(match[2]);
  const lastDay = new Date(Date.UTC(year, Number(month), 0)).getUTCDate();
  const statementDate = `${year}-${month}-${String(lastDay).padStart(2, "0")}`;
  const accurateTo = matchOne(
    statementText,
    /accurate to (\d{2}:\d{2}) \(UTC\) on the last day of the month/u,
  );

  return {
    periodStart: `${year}-${month}-01T00:00:00.000Z`,
    periodEnd: `${statementDate}T${accurateTo || "23:59"}:00.000Z`,
    statementDate,
  };
}

function parseAccountIds(statementText) {
  const ids = {};
  const overviewText = statementText.split(/\n\s*Overview\s*\n/u).at(1) ?? statementText;
  const accountIdMatches = [...overviewText.matchAll(/Account ID:\s*(\d+)/gu)].map(
    (match) => match[1],
  );

  ids.invest = matchOne(
    statementText,
    /Trading 212 Invest\s+Account ID:\s*(\d+)/u,
  );

  if (accountIdMatches.length >= 4) {
    ids.invest ||= accountIdMatches[0];
    ids["stocks-isa"] = accountIdMatches[1] ?? "";
    ids["cash-isa"] = accountIdMatches[2] ?? "";
    ids.cfd = accountIdMatches[3] ?? "";
    return ids;
  }

  ids["stocks-isa"] = accountIdMatches[0] ?? "";
  ids["cash-isa"] = accountIdMatches[1] ?? "";
  ids.cfd = accountIdMatches[2] ?? "";

  return ids;
}

function parseOverview(statementText) {
  const overviewText = sectionBetween(
    statementText,
    "Overview",
    /(?:Invest|Stocks ISA|Cash ISA|CFD) account -/u,
  );

  if (/Trading 212 Invest\s+Trading 212 Stocks ISA/u.test(overviewText)) {
    return parseRowBasedOverview(overviewText);
  }

  const groups = [];
  let current = {};

  for (const match of overviewText.matchAll(
    /(Deposits|Withdrawals|Realised return|Open return(?: change)?|Dividends|Interest on cash|Interest paid|Cashback|FX Fee|Third-party fees|Closed result|Open result(?: change)?|Account value|Account balance|Dividend adjustments|Overnight interest|Margin requirement|Available margin)\s+£\s*(-?[\d,]+(?:\.\d+)?)/gu,
  )) {
    current[match[1]] = money(match[2]);

    if (match[1] === "Account value" || match[1] === "Account balance") {
      groups.push(current);
      current = {};
    }
  }

  return groups;
}

function parseRowBasedOverview(overviewText) {
  const groups = ACCOUNT_CONFIGS.map(() => ({}));
  const metricPattern =
    /(Deposits|Withdrawals|Realised return|Open return(?: change)?|Dividends|Interest on cash|Interest paid|Cashback|FX Fee|Third-party fees|Closed result|Open result(?: change)?|Account value|Account balance|Dividend adjustments|Overnight interest|Margin requirement|Available margin)\s+£\s*(-?[\d,]+(?:\.\d+)?)/gu;

  lines(overviewText)
    .filter((line) => !/Trading 212|Account ID|CUSTOMER|^\d+\/\d+$/u.test(line))
    .forEach((line) => {
      [...line.matchAll(metricPattern)].forEach((match, index) => {
        if (!groups[index] || groups[index][match[1]] !== undefined) {
          return;
        }

        groups[index][match[1]] = money(match[2]);
      });
    });

  return groups;
}

function groupPagesBySection(pages) {
  const grouped = new Map();
  let current = null;

  pages.forEach((pageText) => {
    const classified = classifyPage(pageText);

    if (classified) {
      current = classified;
    } else if (!current) {
      current = null;
    }

    if (!current) {
      return;
    }

    const accountSections = grouped.get(current.accountKey) ?? new Map();
    const sectionPages = accountSections.get(current.section) ?? [];

    sectionPages.push(pageText);
    accountSections.set(current.section, sectionPages);
    grouped.set(current.accountKey, accountSections);
  });

  return grouped;
}

function classifyPage(pageText) {
  for (const config of ACCOUNT_CONFIGS) {
    const accountPattern = new RegExp(`${escapeRegExp(config.label)} account -`, "iu");

    if (!accountPattern.test(pageText)) {
      continue;
    }

    const lower = pageText.toLowerCase();
    const section = SECTION_MATCHERS.find(([, marker]) => lower.includes(marker))?.[0];

    if (section) {
      return { accountKey: config.key, section };
    }
  }

  return null;
}

function createAccount(config, sourceId) {
  return {
    id: `trading212:${sourceId}`,
    name: config.displayName,
    type: config.type,
    institution: "Trading 212",
    accountKind: "actual",
    parentAccountId: "",
    currency: "GBP",
    openingBalance: 0,
    manualBalance: null,
    sourceProvider: PROVIDER,
    sourceId,
    accountRole: "account",
  };
}

function parseCashBreakdown(sectionText, account, overview, statement) {
  const cashBalance =
    matchMoney(sectionText, /GBP cash\s+(-?[\d,]+(?:\.\d+)?)/u) ??
    matchMoney(sectionText, /VALUE\s+(-?[\d,]+(?:\.\d+)?)\s+\1/u) ??
    null;
  const accountValue =
    overview["Account value"] ?? overview["Account balance"] ?? cashBalance;
  const qmmfNotes = parseQmmfNotes(sectionText);
  const overviewNotes = Object.entries(overview)
    .map(([label, value]) => `${label}: ${formatMoney(value)}`)
    .join("; ");
  const notes = [
    overviewNotes ? `Overview: ${overviewNotes}` : "",
    cashBalance !== null ? `Cash balance: ${formatMoney(cashBalance)}` : "",
    qmmfNotes ? `QMMFs: ${qmmfNotes}` : "",
  ]
    .filter(Boolean)
    .join("\n");

  if (accountValue === null || accountValue === undefined) {
    return { hasCash: false, balance: null, warnings: [] };
  }

  return {
    hasCash: true,
    balance: {
      id: `${SOURCE_TYPE}:${account.sourceId}:cash:${statement.statementDate}`,
      accountId: account.id,
      accountRole: account.accountRole,
      date: statement.statementDate,
      balance: accountValue,
      currency: "GBP",
      sourceType: SOURCE_TYPE,
      sourceProvider: PROVIDER,
      sourceId: `${account.sourceId}:account-value`,
      notes,
    },
    warnings: [],
  };
}

function parseQmmfNotes(sectionText) {
  return lines(sectionText)
    .filter((line) => /\b[A-Z]{2}[A-Z0-9]{10}\b/u.test(line))
    .map((line) => line.replace(/\s+/gu, " ").trim())
    .slice(0, 12)
    .join("; ");
}

function parseCashTransactions(sectionPages, account, statement) {
  return sectionPages.flatMap((pageText, pageIndex) => {
    const lineRows = lines(pageText)
      .map((line, index) => {
        const parsed = parseTransactionLine(line);

        return parsed
          ? createCashTransactionRow({
              parsed,
              account,
              statement,
              index: pageIndex * 1000 + index,
              raw: line,
            })
          : null;
      })
      .filter(Boolean);

    if (lineRows.length > 0) {
      return lineRows;
    }

    return parseColumnarCashTransactions(pageText, account, statement, pageIndex);
  });
}

function parseColumnarCashTransactions(sectionText, account, statement, pageIndex) {
  const times = allMatches(sectionText, /20\d{2}-\d{2}-\d{2}\s+\d{2}:\d{2}:\d{2}/gu);
  const types = extractTransactionTypes(sectionText);
  const amounts = extractTransactionAmounts(sectionText, times.length);
  const currencies = extractTransactionCurrencies(sectionText, times.length);
  const count = Math.min(times.length, types.length, amounts.length);

  return Array.from({ length: count }, (_, index) =>
    createCashTransactionRow({
      parsed: {
        time: times[index],
        type: types[index],
        currency: currencies[index] ?? "GBP",
        amount: amounts[index],
      },
      account,
      statement,
      index: pageIndex * 1000 + index,
      raw: "",
    }),
  );
}

function createCashTransactionRow({ parsed, account, statement, index, raw }) {
  return {
    id: `${SOURCE_TYPE}:${account.sourceId}:cash:${index}:${parsed.time}`,
    sourceId: `${account.sourceId}:cash:${parsed.time}:${index}`,
    sourceType: SOURCE_TYPE,
    sourceProvider: PROVIDER,
    rowNumber: index + 1,
    account: account.id,
    date: parsed.time.slice(0, 10),
    amount: parsed.amount,
    description: parsed.type,
    merchant: "Trading 212",
    category: categoryForTransactionType(parsed.type),
    tag: "",
    notes: `Trading 212 ${account.name}; ${parsed.currency}; statement ${statement.statementDate}`,
    currency: parsed.currency,
    isGenerated: false,
    generatedFromId: "",
    accountRole: account.accountRole,
    raw: { line: raw },
    appliedRuleIds: [],
  };
}

function parseTransactionLine(line) {
  const normalized = text(line);

  if (!/^20\d{2}-\d{2}-\d{2}\s+\d{2}:\d{2}:\d{2}\b/u.test(normalized)) {
    return null;
  }

  const type = TRANSACTION_TYPES.find((candidate) =>
    new RegExp(`\\b${escapeRegExp(candidate)}(?:\\s*\\([^)]*\\))?\\b`, "iu").test(
      normalized,
    ),
  );
  const amountMatch = normalized.match(/\b(GBP|USD|EUR)\s+(-?[\d,]+(?:\.\d+)?)/u);

  if (!type || !amountMatch) {
    return null;
  }

  const typeWithDetail =
    normalized.match(new RegExp(`${escapeRegExp(type)}\\s*\\([^)]*\\)`, "iu"))?.[0] ??
    type;

  return {
    time: normalized.slice(0, 19),
    type: typeWithDetail,
    currency: amountMatch[1],
    amount: money(amountMatch[2]),
  };
}

function extractTransactionTypes(sectionText) {
  const typePattern = new RegExp(
    `\\b(${TRANSACTION_TYPES.map(escapeRegExp).join("|")})(?:\\s*\\([^)]*\\))?\\b`,
    "giu",
  );

  return [...sectionText.matchAll(typePattern)]
    .map((match) => match[0]);
}

function extractTransactionAmounts(sectionText, expectedCount) {
  const formattedPounds = [...sectionText.matchAll(/£\s*-?[\d,]+(?:\.\d+)?/gu)].map(
    (match) => money(match[0]),
  );

  if (formattedPounds.length >= expectedCount) {
    return formattedPounds.slice(0, expectedCount);
  }

  const currencyPairs = [...sectionText.matchAll(/\b(?:GBP|USD|EUR)\s+(-?[\d,]+(?:\.\d+)?)/gu)]
    .map((match) => money(match[1]));

  if (currencyPairs.length >= expectedCount) {
    return currencyPairs.slice(0, expectedCount);
  }

  const amountBlock = sectionAfter(sectionText, "CURRENCY AMOUNT")
    .split(/ISSUING COUNTRY|INSTRUMENT CURRENCY|CUSTOMER ID/u)[0];

  return numericValues(amountBlock).slice(0, expectedCount);
}

function extractTransactionCurrencies(sectionText, expectedCount) {
  const currencies = allMatches(sectionText, /\b(?:GBP|USD|EUR)\b/gu);

  if (currencies.length >= expectedCount) {
    return currencies.slice(0, expectedCount);
  }

  return Array.from({ length: expectedCount }, () => "GBP");
}

function parseExecutedTrades(sectionText, account, statement) {
  if (/No data available/iu.test(sectionText)) {
    return [];
  }

  const rows = lines(sectionText)
    .map((line, index) => parseTradeLine(line, account, statement, index))
    .filter(Boolean);

  if (rows.length > 0) {
    return rows;
  }

  return parseColumnarTrades(sectionText, account, statement);
}

function parseTradeLine(line, account, statement, index) {
  const normalized = text(line);
  const match = normalized.match(
    /^(20\d{2}-\d{2}-\d{2}\s+\d{2}:\d{2}:\d{2})\s+([A-Z0-9.]+)\s+([A-Z]{2}[A-Z0-9]{10})\s+([A-Z]{3}|GBX)\s+(\d{6,})\s+(Buy|Sell)\s+(-?[\d,]+(?:\.\d+)?)(.*)$/u,
  );

  if (!match) {
    return null;
  }

  const [, time, instrument, isin, instrumentCurrency, orderId, direction, quantity, rest] =
    match;
  const values = numericValues(rest);
  const valueGbp = values.at(-1) ?? 0;
  const signedAmount = direction === "Buy" ? -Math.abs(valueGbp) : Math.abs(valueGbp);

  return createTradeRow({
    account,
    statement,
    index,
    time,
    instrument,
    isin,
    instrumentCurrency,
    orderId,
    direction,
    quantity: money(quantity),
    amount: signedAmount,
    line,
  });
}

function parseColumnarTrades(sectionText, account, statement) {
  const times = allMatches(sectionText, /20\d{2}-\d{2}-\d{2}\s+\d{2}:\d{2}:\d{2}/gu);
  const instruments = parseInstrumentPairs(sectionText);
  const orderIds = allMatches(sectionText, /\b\d{8,}\b/gu);
  const directions = [...sectionText.matchAll(/\b(Buy|Sell)\s+(-?[\d,]+(?:\.\d+)?)/gu)];
  const values = valuesAfterLastValueHeading(sectionText);
  const count = Math.min(times.length, instruments.length, directions.length);

  return Array.from({ length: count }, (_, index) => {
    const direction = directions[index][1];
    const valueGbp = values[index] ?? 0;

    return createTradeRow({
      account,
      statement,
      index,
      time: times[index],
      instrument: instruments[index].instrument,
      isin: instruments[index].isin,
      instrumentCurrency: instruments[index].currency,
      orderId: orderIds[index] ?? `row-${index + 1}`,
      direction,
      quantity: money(directions[index][2]),
      amount: direction === "Buy" ? -Math.abs(valueGbp) : Math.abs(valueGbp),
      line: "",
    });
  });
}

